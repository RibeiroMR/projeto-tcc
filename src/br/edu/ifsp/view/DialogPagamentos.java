    private void btnQuitarParcelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarParcelaActionPerformed
        if (cboxFormaPagamento.getSelectedIndex() != 0) {
            String opt[] = {"Sim", "Não"};

            int op = JOptionPane.showOptionDialog(this, "Confirmar pagamento da parcela?", "Confirme a quitação da parcela", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opt, opt[1]);

            if (op == JOptionPane.YES_OPTION) {
                boolean anterior = true;

                for (int i = 0; i < tableListaPagamento.getRowCount(); i++) {
                    if ((tableListaPagamento.getValueAt(i, 6).equals("Pago")) && (!anterior)) {
                        anterior = true;
                    } else if (((tableListaPagamento.getValueAt(i, 6).equals("Pendente")) || (tableListaPagamento.getValueAt(i, 6).equals("Atrasado")))
                            && (anterior)) {
                        pagamento = new Pagamento();

                        pagamento.setCodigo(Integer.parseInt(String.valueOf(tableListaPagamento.getValueAt(i, 0))));
                        pagamento.setDatapagamento(new Date(System.currentTimeMillis()));
                        pagamento.setFormaPagamento(Integer.parseInt(cboxFormaPagamento.getSelectedItem().toString().split("-")[0]));
                        pagamento.setSituacao("Pago");

                        anterior = false;

                        pagamentoControl.quitarPagamento(pagamento);
                    }
                }
                exportarListaPagamento();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Defina a forma de pagamento no campo de seleção!");
        }
    }//GEN-LAST:event_btnQuitarParcelaActionPerformed

