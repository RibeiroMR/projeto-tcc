    public void exportarListaPagamento(ArrayList<EntidadesPagamento> listaPagmentosVenda) {
        DefaultTableModel modelo = (DefaultTableModel) tableListaPagamento.getModel();
        modelo.setNumRows(0);

        int cont = listaPagmentosVenda.size();

        SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");

        lblNomeCliente.setText(String.valueOf(listaPagmentosVenda.get(0).getClientesObj().getNome()));

        lblCodigoVenda.setText(String.valueOf(listaPagmentosVenda.get(0).getVendaObj().getCodigo()));
        lblDataPedidoVenda.setText(form.format(listaPagmentosVenda.get(0).getVendaObj().getDataPedido()));
        lblTotalVenda.setText("R$ " + String.valueOf(listaPagmentosVenda.get(0).getVendaObj().getTotal()).replace(".", ","));
        lblQtdPagamentoVenda.setText(String.valueOf(listaPagmentosVenda.get(0).getVendaObj().getQtdpagamento()));

        for (int i = 0; i < cont; i++) {

            formaPagamento = formaPagamentoControl.buscarCodigoFormaPagamento(String.valueOf(listaPagmentosVenda.get(i).getPagamentosObj().getFormaPagamento()));
            modelo.addRow(new Object[]{
                listaPagmentosVenda.get(i).getPagamentosObj().getCodigo(),
                listaPagmentosVenda.get(i).getPagamentosObj().getNumero(),
                listaPagmentosVenda.get(i).getPagamentosObj().getPreco(),
                listaPagmentosVenda.get(i).getPagamentosObj().getDatavencimento(),
                listaPagmentosVenda.get(i).getPagamentosObj().getDatapagamento(),
                formaPagamento.getForma(),
                listaPagmentosVenda.get(i).getPagamentosObj().getSituacao()
            });
        }
        organizarJtableVendas();
    }

    public void exportarListaPagamento() {
        DefaultTableModel modelo = (DefaultTableModel) tableListaPagamento.getModel();
        modelo.setNumRows(0);

        listaPagmentosVenda = new ArrayList<>();
        listaPagmentosVenda = pagamentoControl.consultarPagamentoVenda(lblCodigoVenda.getText());

        int cont = listaPagmentosVenda.size();

        SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");

        lblNomeCliente.setText(String.valueOf(listaPagmentosVenda.get(0).getClientesObj().getNome()));

        lblCodigoVenda.setText(String.valueOf(listaPagmentosVenda.get(0).getVendaObj().getCodigo()));
        lblDataPedidoVenda.setText(form.format(listaPagmentosVenda.get(0).getVendaObj().getDataPedido()));
        lblTotalVenda.setText("R$ " + String.valueOf(listaPagmentosVenda.get(0).getVendaObj().getTotal()).replace(".", ","));
        lblQtdPagamentoVenda.setText(String.valueOf(listaPagmentosVenda.get(0).getVendaObj().getQtdpagamento()));

        for (int i = 0; i < cont; i++) {

            formaPagamento = formaPagamentoControl.buscarCodigoFormaPagamento(String.valueOf(listaPagmentosVenda.get(i).getPagamentosObj().getFormaPagamento()));
            modelo.addRow(new Object[]{
                listaPagmentosVenda.get(i).getPagamentosObj().getCodigo(),
                listaPagmentosVenda.get(i).getPagamentosObj().getNumero(),
                listaPagmentosVenda.get(i).getPagamentosObj().getPreco(),
                listaPagmentosVenda.get(i).getPagamentosObj().getDatavencimento(),
                listaPagmentosVenda.get(i).getPagamentosObj().getDatapagamento(),
                formaPagamento.getForma(),
                listaPagmentosVenda.get(i).getPagamentosObj().getSituacao()
            });
        }
        organizarJtableVendas();
    }

    private void listarFormaPagamento() {
        listaFormaPagamento = formaPagamentoControl.listar();

        cboxFormaPagamento.removeAllItems();

        cboxFormaPagamento.addItem("- Selecione a Forma de Pagamento -");

        for (FormaPagamento listaFp : listaFormaPagamento) {
            cboxFormaPagamento.addItem(listaFp.toString());
        }
    }

    private void organizarJtableVendas() {
        tableListaPagamento.getColumnModel().getColumn(2).setCellRenderer(NumberRenderer.getCurrencyRenderer());
        tableListaPagamento.getColumnModel().getColumn(3).setCellRenderer(FormatRenderer.getDateRenderer());
        tableListaPagamento.getColumnModel().getColumn(4).setCellRenderer(NumberRenderer.getDateRenderer());
    }

    public static void centreWindow(Window frame) {
        Dimension dimension = Toolkit.getDefaultToolkit().getScreenSize();
        int x = (int) ((dimension.getWidth() - frame.getWidth()) / 2);
        int y = (int) ((dimension.getHeight() - frame.getHeight()) / 2);
        frame.setLocation(x, y);
    }
