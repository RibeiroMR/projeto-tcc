    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (rowSelectedInTable != -1) {
            novo = false;

            btnNovo.setEnabled(false);
            btnAlterar.setEnabled(false);
            habilitarCampos(true);
            btnGravar.setEnabled(true);
            btnCancelar.setEnabled(true);
            
            tableProduto.setEnabled(false);
            tableProduto.setRowSelectionInterval(rowSelectedInTable, rowSelectedInTable);
            
            txtPesquisa.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto da lista!", "Alteração de Dados", 1);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed


