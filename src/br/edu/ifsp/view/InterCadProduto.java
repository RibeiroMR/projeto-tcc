    private void listaProduto() {
        String sql = "SELECT * FROM produto";

        try {
            pst = conexao.prepareStatement(sql);

            rs = pst.executeQuery();

            tableProduto.setModel(DbUtils.resultSetToTableModel(rs));

            organizarJtable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void pesquisarProduto() {
        String sql = "SELECT * FROM produto WHERE descricao LIKE ?";

        try {
            pst = conexao.prepareStatement(sql);

            pst.setString(1, "%" + txtPesquisa.getText() + "%");

            rs = pst.executeQuery();

            tableProduto.setModel(DbUtils.resultSetToTableModel(rs));

            organizarJtable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void setCampos() {
        rowSelectedInTable = tableProduto.getSelectedRow();

        txtDescricao.setText(tableProduto.getModel().getValueAt(rowSelectedInTable, 1).toString());
        txtMarca.setText(tableProduto.getModel().getValueAt(rowSelectedInTable, 2).toString());
        txtModelo.setText(tableProduto.getModel().getValueAt(rowSelectedInTable, 3).toString());

        String preco = tableProduto.getModel().getValueAt(rowSelectedInTable, 4).toString();
        String precoFormat = preco.replace("R$", "").replace(" ", "").replace(".", ",");
        ftxtPreco.setText(precoFormat);

        String precoreal = tableProduto.getModel().getValueAt(rowSelectedInTable, 5).toString();
        String precorealFormat = precoreal.replace("R$", "").replace(" ", "").replace(".", ",");
        ftxtPrecoReal.setText(precorealFormat);

        String estoque = tableProduto.getModel().getValueAt(rowSelectedInTable, 6).toString();
        String estoqueFormat = estoque.replace(" ", "").replace(".", ",");
        ftxtEstoque.setText(estoqueFormat);

        String estoqueminimo = tableProduto.getModel().getValueAt(rowSelectedInTable, 7).toString();
        String estMinFormat = estoqueminimo.replace(" ", "").replace(".", ",");
        ftxtEstoqueMinimo.setText(estMinFormat);
    }

    private void adicionar() {
        if ((txtDescricao.getText().isEmpty()) || (txtMarca.getText().isEmpty())
                || (txtModelo.getText().isEmpty()) || (ftxtPreco.getText().isEmpty()) || (ftxtPrecoReal.getText().isEmpty()) || (ftxtEstoque.getText().isEmpty())
                || (ftxtEstoqueMinimo.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios (*) !");
        }else if (0 > Double.parseDouble(ftxtEstoque.getText().replace(".", "").replace(",", ".")) || 0 > Double.parseDouble(ftxtEstoqueMinimo.getText().replace(".", "").replace(",", "."))
                || 0 > Double.parseDouble(ftxtPreco.getText().replace(".", "").replace(",", ".")) || 0 > Double.parseDouble(ftxtPrecoReal.getText().replace(".", "").replace(",", "."))) {
            JOptionPane.showMessageDialog(null, "Não informe valores abaixo que Zero (*) !");
        } else {
            int op = JOptionPane.showInternalConfirmDialog(getInstance(), "Prosseguir com as inserções feitas?", "Registro de produto",
                    JOptionPane.YES_NO_OPTION);

            if (op == JOptionPane.YES_NO_OPTION) {
                Produto produto = new Produto();

                ProdutoControle prodControl = new ProdutoControle();

                produto.setDescricao(txtDescricao.getText());
                produto.setMarca(txtMarca.getText());
                produto.setModelo(txtModelo.getText());

                String preco = ftxtPreco.getText();
                String precoform = preco.replace(".", "").replace(",", ".");
                produto.setPreco(Double.parseDouble(precoform));

                String precoreal = ftxtPrecoReal.getText();
                String precorealform = precoreal.replace(".", "").replace(",", ".");
                produto.setPrecoreal(Double.parseDouble(precorealform));

                String estoque = ftxtEstoque.getText();
                String estoqueform = estoque.replace(".", "").replace(",", ".");
                produto.setEstoque(Double.parseDouble(estoqueform));

                String estoqueMin = ftxtEstoqueMinimo.getText();
                String estMinform = estoqueMin.replace(".", "").replace(",", ".");
                produto.setEstoqueminimo(Double.parseDouble(estMinform));

                if (novo) {
                    prodControl.adicionar(produto);
                } else {
                    produto.setCodigo(Integer.parseInt(tableProduto.getModel().getValueAt(rowSelectedInTable, 0).toString()));
                    prodControl.atualizar(produto);
                }

                limparCampos();

                tableProduto.setEnabled(true);
                btnNovo.setEnabled(true);
                btnAlterar.setEnabled(true);

                habilitarCampos(false);

                btnGravar.setEnabled(false);
                btnCancelar.setEnabled(false);

                txtPesquisa.setEnabled(true);
                
                listaProduto();
            }
        }
    }

